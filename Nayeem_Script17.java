package mYPackage2;

public class Nayeem_Script17 {

	public static void main(String[] args) {
		// Arithmetic operator 
		/* 
		 * + : Addition
		 * - : Subtraction
		 * * : Multiplication
		 * / : Division
		 * % : Modulus
		 */
				
		int num1, num2, num3;
				
			num1=15;
			num2=10;
			num3=5;
				
			System.out.println("Addition of three num:"+(num1+num2+num3));
			System.out.println("Subtraction of three num:"+((num1-num2)-(num3)));
			System.out.println("Product/Multiplication of three num:"+(num1*num2*num3));
			System.out.println("Division of two num:"+(num1/num2));
			System.out.println("Modulas of two num:"+(num1%num2));
					
	}

}

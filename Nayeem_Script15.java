package mYPackage2;

public class Nayeem_Script15 {

	public static void main(String[] args) {
		// Arithmetic Operator
		/* 
		 * + : Addition
		 * - : Subtraction
		 * * : Multiplication
		 * / : Division
		 * % : Modulus
		 *
		 * ++ : Increment Opt
		 * -- : Decrement Opt
		 * 
		 * > : Greater than
		 * >= : Greater than or equal to
		 * <  : Less Than
		 * <= : Less than or equal t0
		 * =  : Assign to or Initialization
		 * == : Equal to
		 * != : Not Equal to
		 * < > : Not Equal to
		 *
		 */
		
		int a,b,c,c1,c2,d;
		
		a=75;
		b=50;
		
		a =a+b; //125
		c =a+b; //125+50=175
		d =c-a; // 175-125
		
		c1=(a+b)*2;   // BODMAS
		c2=a+b*2;
		
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(a);
		System.out.println(c);
		System.out.println(d);
	}

}

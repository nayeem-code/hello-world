public class MyClass1 {
	/* Escape Sequence: \n, \t, \''..\'',
	 *
	 */

	public static void main(String[] args) {				  // Escape Sequence in a Double quotation
		System.out.println("Hellow Nayeem \n 551-229-7247");  // \n = new line creation in a single line code
		System.out.println("1 \t 2"); 						  // \t = Tab create 4 space
		System.out.println("3 \t 4");
		System.out.println(" \"Java Programming\" ");		  // \" .. \" = Print with "Double Quotation" symbol
	}

}

package mYPackage1;

public class MyClass8 {

	public static void main(String[] args) {
		// Assignment Operator
		
		int x = 3;
		int y = 2;
		
		x+=y; // x = x + y = 5
		System.out.println("x ="+x);
		
		x-=y; // x = x - y = 3
		System.out.println("x ="+x);
		
		x*=y; // x = x * y = 6
		System.out.println("x ="+x);
		
		x/=y; // x = x / y = 3
		System.out.println("x ="+x);
		
		x%=y; // x = x % y = 1 (Vag Shesh)
		System.out.println("x ="+x);
		
	}
}

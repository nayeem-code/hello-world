package mYPackage1;

public class MyClass13 {

	public static void main(String[] args) {
		// Unary Operator = +x(Unary Plus), -x (Unary Minus),
		
		int x = 10;
		int result;
		
		result = +x;  							// Convert to positive value
		System.out.println("result ="+result);
		
		result = -x;
		System.out.println("result ="+result);	// Convert to Negative value
		
	}

}
